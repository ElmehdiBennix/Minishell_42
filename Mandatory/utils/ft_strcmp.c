#include "../inc/minishell.h"

int	ft_strcmp(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0' && s1[i] == s2[i]) // now its safe to use
		i++;
	return (s1[i] - s2[i]);
}